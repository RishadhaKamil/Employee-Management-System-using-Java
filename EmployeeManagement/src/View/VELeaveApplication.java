    private void btnVAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVAActionPerformed
       if(txtEid.equals(""))
       { 
           JOptionPane.showMessageDialog(null,"Please enter your Employee ID to see the Approval");
       }
       else
       {
           int id=Integer.parseInt(txtEid.getText());
         try
        { Class.forName("com.mysql.cj.jdbc.Driver");
         Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/EmployeeDB","root","");
            JasperDesign design=JRXmlLoader.load("C:\\Users\\user\\Desktop\\EAD\\EmployeeManagement\\src\\View\\ViewApprovalReport.jrxml");
            JRDesignQuery designquery=new JRDesignQuery();
            String myquery="Select * from tblLeave where empid='"+id+"'";
            designquery.setText(myquery);
            design.setQuery(designquery);
            
            JasperReport report=JasperCompileManager.compileReport(design);
            JasperPrint print=JasperFillManager.fillReport(report,null,con);
            
            JasperViewer.viewReport(print);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
       }
       
    }//GEN-LAST:event_btnVAActionPerformed

