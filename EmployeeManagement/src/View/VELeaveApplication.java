    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int id=Integer.parseInt(txtEid.getText());
         Date dob1;
         Date dob2;
         String reason=txtReason.getText();
         String mydob2="";
         String mydob="";
       
       try{
           dob1=dtpStart.getDate();
        DateFormat format1=new SimpleDateFormat("yyyy-MM-dd");
        mydob=format1.format(dob1);
        
        dob2=dtpEnd.getDate();
        DateFormat format2=new SimpleDateFormat("yyyy-MM-dd");
        mydob2=format2.format(dob2);
       
       }
       catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,e.getMessage());
       }
       if(id==0||mydob.equals("")||mydob2.equals("")|| reason.equals(""))
       {
           JOptionPane.showMessageDialog(null,"All the fields should be filled");
       }
       
       
        CELeaveApplication csc =new CELeaveApplication();
        csc.checkApp(id,mydob,mydob2,reason);
    }//GEN-LAST:event_jButton2ActionPerformed

