    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
         String name=txtName.getText();
        String email=txtEmail.getText();
        String gender="";
        Date dob;
        String pwd=txtPwd.getText();
        String adrs=txtAddress.getText();
        String qualification=cmbQualification.getSelectedItem().toString();
        String mydob="";
        String status=txtStatus.getText();
       double  bsalary=Double.parseDouble(txtBasicSalary.getText());
        
       if(rdFemale.isSelected())
       { gender="Female";
       }
       else if(rdMale.isSelected())
       { gender="Male";
       }
       
       try{
           dob=dtp1.getDate();
        DateFormat format1=new SimpleDateFormat("yyyy-MM-dd");
        mydob=format1.format(dob);
       
       }
       catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,e.getMessage());
       }
       if(name.equals("")||email.equals("")||gender.equals("")|| mydob.equals("")||pwd.equals("")||adrs.equals("")||status.equals("")||qualification.equals("")||(txtBasicSalary.getText()).equals(""))
       {
           JOptionPane.showMessageDialog(null,"All the fields should be filled");
       }
       
       
        CManageEmp csc =new CManageEmp();
        csc.createEmp(name,email,gender,pwd,status,adrs,qualification,mydob,bsalary);
    }//GEN-LAST:event_btnAddActionPerformed

