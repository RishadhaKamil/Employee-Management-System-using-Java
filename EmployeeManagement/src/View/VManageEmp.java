    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        int id=Integer.parseInt(txtid.getSelectedItem().toString());
      
        CManageEmp gsc= new CManageEmp();
       
        
       
        try{
            ResultSet rst=gsc.getData(id);
            String gender="",mydob="";
            while(rst.next())
            {  
               txtName.setText(rst.getString(1)); 
                txtEmail.setText(rst.getString(2));
                txtPwd.setText(rst.getString(3));
                 gender=rst.getString(4);
                 mydob=rst.getString(5);
                txtAddress.setText(rst.getString(6));
                txtStatus.setText(rst.getString(7));
                txtBasicSalary.setText(String.valueOf(rst.getDouble(8)));
                cmbQualification.addItem(rst.getString(9));
            }
            DateFormat sdf=new SimpleDateFormat("yyyy-mm-dd");
            Date dob=sdf.parse(mydob);
            dtp1.setDate(dob);
          
            if(gender=="male")
            {
                rdMale.setSelected(true);
            }
            else
            {
              rdFemale.setSelected(true);
            }
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

